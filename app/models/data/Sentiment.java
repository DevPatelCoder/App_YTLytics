package models.data;

/**
 * Enum representing the sentiment of a description or text.
 * It includes three possible sentiment values: HAPPY, SAD, and NEUTRAL.
 *
 */
public enum Sentiment {

    /** Represents a positive sentiment. */
    HAPPY,

    /** Represents a negative sentiment. */
    SAD,

    /** Represents a neutral sentiment. */
    NEUTRAL;
}
